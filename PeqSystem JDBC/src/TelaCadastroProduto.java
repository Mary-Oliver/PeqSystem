    private void atualizarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTableTabela.getModel();
        modelo.setRowCount(0); 

        for (Produto produto : listaProdutos) {
            Object[] linha = {
                produto.getNome(),
                produto.getCategoria(),
                produto.getPreco(),
                produto.getQuantidade()
            };
            modelo.addRow(linha);
        }
    }

   private void listarProdutosPorNome(String nomeBusca) {
    try {
        Connection con = Conexao.conectar();

        String sql = "SELECT * FROM Produto WHERE Descricao LIKE ?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setString(1, "%" + nomeBusca + "%");
        ResultSet rs = stmt.executeQuery();
        
        DefaultTableModel modelo = (DefaultTableModel) jTableTabela.getModel();
        modelo.setRowCount(0); 
        
        while (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getString("Descricao"),
                rs.getString("Categoria"),
                rs.getDouble("Preco"),
                rs.getInt("Estoque_idEstoque") 
            });
        }
        
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao buscar produtos: " + e.getMessage());
    }
}


    
