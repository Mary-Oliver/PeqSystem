    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                        
                                        
    String nome = jTextFieldNome.getText();
    String categoria = jComboBoxCategoria.getSelectedItem().toString();
    double preco = Double.parseDouble(jTextFieldPreco.getText());
    int quantidade = Integer.parseInt(jTextFieldEstoque.getText());

    Connection conn = null;
    PreparedStatement stmt = null;

    try {
        conn = Conexao.conectar();
        if (conn != null) {
            String sql = "INSERT INTO Produto (Descricao, Preco, Categoria, Quantidade) VALUES (?, ?, ?, ?)";
            stmt = conn.prepareStatement(sql);
            stmt.setString(1, nome);
            stmt.setDouble(2, preco);
            stmt.setString(3, categoria);
            stmt.setInt(4, quantidade); 

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Produto salvo com sucesso!");

            atualizarTabela();
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao conectar com o banco de dados.");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar: " + ex.getMessage());
    } finally {
        try {
            if (stmt != null) {
                stmt.close();
            }
            if (conn != null) {
                conn.close();
            }
        } catch (SQLException ex) {
            System.out.println("Erro ao fechar recursos: " + ex.getMessage());
        }
    }



    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTableTabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTabelaMouseClicked

        System.out.println("Linha clicada");
        indiceSelecionado = jTableTabela.getSelectedRow();

        if (indiceSelecionado != -1) {
            Produto selecionado = listaProdutos.get(indiceSelecionado);
            jTextFieldNome.setText(selecionado.getNome());
            jComboBoxCategoria.setSelectedItem(selecionado.getCategoria());
            jTextFieldPreco.setText(String.valueOf(selecionado.getPreco()));
            jTextFieldEstoque.setText(String.valueOf(selecionado.getQuantidade()));
        }


    }//GEN-LAST:event_jTableTabelaMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (indiceSelecionado != -1) {
            int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este produto?", "Confirmação", JOptionPane.YES_NO_OPTION);

            if (confirmacao == JOptionPane.YES_OPTION) {
                listaProdutos.remove(indiceSelecionado);
                atualizarTabela();
                limparCampos();
                indiceSelecionado = -1;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um produto para excluir.");
        }


    }//GEN-LAST:event_jButton3ActionPerformed

